const createContext = () => {
  return Object.freeze({
    prepare(history: Message[], prompt: string, asService: boolean = false): Message[] {
      return [
        SYSTEM_PROMPT,
        ...history,
        {
          role: asService ? ('system' as const) : ('user' as const),
          content: prompt,
        },
      ];
    },
  });
};

export { createContext };
